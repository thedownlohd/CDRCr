#' Area Function
#'
#' This is an example function named 'area'
#' which prints the area of a given rectangle.
#'
#' @param length A given length
#' @param width A given width
#' @return The product of \code{length} and \code{width}
#' @examples
#' area(3,4)
#' area(5,2)
#' @export

area <- function(length, width) {
  length * width
}

area(3,4)

